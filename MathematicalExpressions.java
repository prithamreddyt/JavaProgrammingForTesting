            System.out.println(sh+s+ss);
            System.out.println(sh+shh+s+ss);
            System.out.println(s+ss+sh+shh);
            System.out.println(s+ss+(sh+shh));

            System.out.println("");
            System.out.println("*************");

        System.out.println("addition operation with strings and long");
        System.out.println("");

