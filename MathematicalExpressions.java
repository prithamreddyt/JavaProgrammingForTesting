            System.out.println(c+cc+s+ss);
            System.out.println(s+ss+c+cc);
            System.out.println(s+ss+(c+cc));

            System.out.println("");
            System.out.println("*************");
        
        
        System.out.println("addition operation with strings and short");
        System.out.println("");

        short sh=123;
